public class Contagem {
    public static void Contagem (String[] args) {
      
        System.out.println("Contagem:");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        System.out.println("\nContagem:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}





